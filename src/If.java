public class If {
    public static void main(String[] args){
        Integer numeroif = 5;
        if (numeroif > 0){
            System.out.println("Es Positivo");
        } else if (numeroif < 0) {
            System.out.println("Es Negativo");
        } else {
            System.out.println("Es 0");
        }
    }
}
